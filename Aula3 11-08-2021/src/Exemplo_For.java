
public class Exemplo_For {

	public static void main(String[] args) {
		for (int i=1; i < 11; i++) { //laço de repetição
			if  (i % 2 == 0) { // confere se o i é par
				System.out.printf("i é par = %d\n", i);
			} else {
				System.out.printf("i é impar = %d\n", i);
			}
		}

	}

}
