/*Faça um programa que receba o salário de um funcionário,
 *  calcule e mostre o novo salário, 
 *  sabendo-se que este sofreu um aumento de 25%.*/

import java.util.Scanner;

public class lista01_04 {

	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		Float sal, nsal; 
		Double aumento;
		
		aumento = 0.25;
		
		System.out.print("Informe o salário atual do funcionário: R$ ");
		sal  = scan.nextFloat();
		
		System.out.println("Salário sofreu aumento de 25%.");
		
		nsal = (float) ((sal * aumento)+ sal);
		System.out.printf("Novo salário do funcionario: R$ %.2f",nsal);
	}

}

